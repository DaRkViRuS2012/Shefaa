import UIKit


extension UIColor{


    public func mainColor()->UIColor{
        return UIColor(red: 84/255, green: 190/255, blue: 192/255, alpha: 1)
    }
    
    public func blackColor()->UIColor{
        return  UIColor(red: 0/255, green: 0/255, blue: 0/255, alpha: 1)
    }
    
    public func SeconderyColor()->UIColor{
        return UIColor(red: 255/255, green: 255/255, blue: 255/255, alpha: 1)
    }
    
    public func cyan()->UIColor{
        return UIColor(red: 75/255, green: 185/255, blue: 209/255, alpha: 1)
    }
    
    public func purpel()->UIColor{
        return  UIColor(red: 83/255, green: 45/255, blue: 62/255, alpha: 1)
    }
    

}
